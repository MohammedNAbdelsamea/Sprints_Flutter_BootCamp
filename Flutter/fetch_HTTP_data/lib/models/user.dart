import 'package:fetch_http_data/models/address.dart';
import 'package:fetch_http_data/models/company.dart';

/// User model class representing a user from the JSONPlaceholder API
class User {
  final int id;
  final String name;
  final String username;
  final String email;
  final Address address;
  final String phone;
  final String website;
  final Company company;

  User({
    required this.id,
    required this.name,
    required this.username,
    required this.email,
    required this.address,
    required this.phone,
    required this.website,
    required this.company,
  });
  /// Factory constructor to create a User instance from JSON
  /// [json] - Map containing the user data from the API
  factory User.fromJson(Map<String, dynamic> json) {
    return User(
      id: json['id'],
      name: json['name'],
      username: json['username'],
      email: json['email'],
      // Convert nested JSON objects to their respective model classes
      address: Address.fromJson(json['address']),
      phone: json['phone'],
      website: json['website'],
      company: Company.fromJson(json['company']),
    );
  }
}





